package com.ibm.smarterplanet.healtcare.smartbabies.model;

public class SexualIntercourseCycle {
	
	private String ovulationStartDate;
	
	private String ovulationEndDate;
	
	private String cycleStartDate;
	
	private String cycleEndDate;

	private String desiredStartBornDate;
	
	private String desiredEndBornDate;

	public String getOvulationStartDate() {
		return ovulationStartDate;
	}

	public void setOvulationStartDate(String ovulationStartDate) {
		this.ovulationStartDate = ovulationStartDate;
	}

	public String getOvulationEndDate() {
		return ovulationEndDate;
	}

	public void setOvulationEndDate(String ovulationEndDate) {
		this.ovulationEndDate = ovulationEndDate;
	}

	public String getCycleStartDate() {
		return cycleStartDate;
	}

	public void setCycleStartDate(String cycleStartDate) {
		this.cycleStartDate = cycleStartDate;
	}

	public String getCycleEndDate() {
		return cycleEndDate;
	}

	public void setCycleEndDate(String cycleEndDate) {
		this.cycleEndDate = cycleEndDate;
	}

	public String getDesiredStartBornDate() {
		return desiredStartBornDate;
	}

	public void setDesiredStartBornDate(String desiredStartBornDate) {
		this.desiredStartBornDate = desiredStartBornDate;
	}

	public String getDesiredEndBornDate() {
		return desiredEndBornDate;
	}

	public void setDesiredEndBornDate(String desiredEndBornDate) {
		this.desiredEndBornDate = desiredEndBornDate;
	}

	
}
